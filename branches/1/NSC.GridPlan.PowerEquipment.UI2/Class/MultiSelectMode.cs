
using System;
namespace DevExpress.XtraVerticalGrid

{
    // 摘要: 
    //     Contains values that specify multiple selection mode in the Vertical Grid
    //     control.
    public enum MultiSelectMode
    {
        // 摘要: 
        //     Enables selection of multiple records.
        RecordSelect = 0,
        //
        // 摘要: 
        //     Enables selection of multiple blocks of cells.
        CellSelect = 1,
        //
        // 摘要: 
        //     Enables selection of multiple rows.
        RowSelect = 2,
    }
}
